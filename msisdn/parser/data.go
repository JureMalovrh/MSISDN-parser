func findCountry(s string) (string, bool){
	res, err := countries[s]
	return res, err
}

/* Check first 4 numbers for CC (max number of numbers). If we find a match, we return Country identifier and country code, else empty string is returned */
func GetCountryFromCountryCode(s string) (string, string) {
	border := 5 

	if len(s) < 5 {
		border = len(s) + 1
	}

	var country string
	var countryCode string
	for i := 1; i < border; i++ {
		if cc, exists := findCountry(s[:i]); exists {
			country = cc
			countryCode = s[:i]
			break;
		}
		
	}
	// if country is not found, return empty string for both countryCode and name	
	if(country == "") {
		return "", ""
	}

	return country, countryCode
}

/* Check first 7 numbers (CC + MNO) for name of MNO. If none of MNO is found, Unknown provider is returned else, MNO identifier is returned and SN is returned as well */
func GetMNOProvider(s string) (string, string) {
	var mno string = "Unknown provider"
	var subscriberNumber = ""
	border := 8 

	if len(s) < 8 {
		border = len(s) + 1
	}

	for i := 1; i < border; i++ {
		if mnoString, exists := findMNOProviders(s[:i]); exists {
			mno = mnoString
			subscriberNumber = s[i:]
			break;
		}
	}

	return mno, subscriberNumber;
}
